    public static String[] getPerfUserNames(int count) {
            String[] userNames = new String[count];
            TreeMap<Character, Character> code = new TreeMap<>();
            code.put('0', 'z');
            code.put('1', 'a');
            code.put('2', 'b');
            code.put('3', 'c');
            code.put('4', 'd');
            code.put('5', 'e');
            code.put('6', 'f');
            code.put('7', 'g');
            code.put('8', 'h');
            code.put('9', 'i');

            for (int i = 0; i < count; i++) {
                String name = "";
                String suffix = String.valueOf(i);
                for (char c : suffix.toCharArray()){
                    name += code.get(c);
                }
                userNames[i] = "ueba_user_" + name;
            }

            System.out.println("$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$\n$$  last user name: " + userNames[userNames.length-1] + "\n$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$");
            return userNames;
    }

